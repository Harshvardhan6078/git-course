# for loop 
# we can use else aloso which is absolutelily optional

# start and increment range not given
for i in range(10):
    print(i,end=" ")
print("\n")    



# start and end range given
for i in range(1,10):
    print(i,end=" ")
print("\n")



# start and end range given with increment
for i in range(1,10,2):
    print(i,end=" ")
print("\n")
    

set1={1,2,3,4,5}
for i in set1:
    print(i,end=" ")
    
